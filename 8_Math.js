// ● Math merupakan class di JavaScript yang berisikan static property dan method untuk operasi
// matematika
// ● Ada banyak sekali static property dan method di Math
// ● Math hanya bisa digunakan untuk tipe data Number, tidak bisa digunakan untuk tipe data BigInt
// ● https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Math
console.info(Math.max(10, 11, 12, 13, 8)); // output: 13
console.info(Math.min(10, 11, 12, 13, 8)); // output: 8
console.info(Math.round(10.5)); // output: 11
console.info(Math.round(10.3)); // output: 10
console.info(Math.floor(10.8)); // output: 10
console.info(Math.ceil(10.3)); // output: 11
